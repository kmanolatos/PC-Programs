    private void yearComboBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_yearComboBoxItemStateChanged
        try {
            if (evt.getStateChange() == ItemEvent.DESELECTED) {
                tableModel.setRowCount(0);
                debtModel = execute.SelectCustomersDebtByYear("select date, reason, xreosi, pistosi, ipolipo, debtYear from customersDebtByYear where id = " + customerModel.id + " and debtYear = " + yearComboBox.getSelectedItem());
                for (CustomerDeptModel item : debtModel) {
                    row = new Vector();
                    row.add(customerModel.customerSurname);
                    row.add(customerModel.customerName);
                    row.add(item.reason);
                    row.add(numberFormatter.format(item.xreosi));
                    row.add(numberFormatter.format(item.pistosi));
                    row.add(numberFormatter.format(item.ipolipo));
                    row.add(item.date);
                    tableModel.addRow(row);
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerDebtByYear.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerDebtByYear.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_yearComboBoxItemStateChanged

    private void deleteDebtYearButtonMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteDebtYearButtonMouseReleased
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Είστε σίγουρος πως θέλετε να διαγράψετε την ανάλυση οφειλών του έτους " + yearComboBox.getSelectedItem() + "?", "Επιβεβαίωση", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            try {
                execute.UpsertOrDelete("delete from customersDebtByYear where id = " + customerModel.id + " and debtYear = " + yearComboBox.getSelectedItem());
            } catch (ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(this,
                        "Η διαγραφή απέτυχε!",
                        "Σφάλμα",
                        JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this,
                        "Η διαγραφή απέτυχε!",
                        "Σφάλμα",
                        JOptionPane.ERROR_MESSAGE);
            }
            yearComboBox.removeItemAt(yearComboBox.getSelectedIndex());
            if (yearComboBox.getItemCount() == 0) {
                yearScrollPane.setVisible(false);
                deleteDebtYearButton.setVisible(false);
            }
            JOptionPane.showMessageDialog(this,
                    "Η διαγραφή έγινε με επιτυχία!");
        }
    }//GEN-LAST:event_deleteDebtYearButtonMouseReleased

