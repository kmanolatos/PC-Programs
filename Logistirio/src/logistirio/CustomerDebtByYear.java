    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        getContentPane().setBackground(Color.GRAY);
        try {
            customerDebtScrollPane.setVisible(false);
            yearScrollPane.setVisible(false);
            backArrowLabel.setVisible(false);
            deleteDebtYearButton.setVisible(false);
            Dimension screenSize = Toolkit.getDefaultToolkit().getScreenSize();
            int w = (int) screenSize.getWidth();
            int h = (int) screenSize.getHeight();
            setSize(w * 90 / 100, h * 83 / 100);
            setLocationRelativeTo(null);
            BufferedImage img = null;
            setLocationRelativeTo(null);
            URL urlToImage = getClass().getResource("/img/backArrow.png");
            try {
                img = ImageIO.read(urlToImage);
            } catch (IOException e) {
                e.printStackTrace();
            }
            Image dimg = img.getScaledInstance(w * 4 / 100, h * 2 / 100, Image.SCALE_SMOOTH);
            ImageIcon imageIcon = new ImageIcon(dimg);
            backArrowLabel.setIcon(imageIcon);
            backArrowLabel.setLocation(w * 3 / 100, h * 3 / 100);
            backArrowLabel.setSize(w * 4 / 100, h * 2 / 100);
            yearScrollPane.setLocation(w * 34 / 100, h * 2 / 100);
            yearScrollPane.setSize(w * 10 / 100, h * 4 / 100);
            Font currentFont = deleteDebtYearButton.getFont();
            deleteDebtYearButton.setFont(currentFont.deriveFont(Font.BOLD, w * 1 / 100));
            deleteDebtYearButton.setLocation(w * 49 / 100, h * 2 / 100);
            deleteDebtYearButton.setSize(w * 10 / 100, h * 4 / 100);
            yearScrollPane.getViewport().setOpaque(false);
            DefaultListCellRenderer dlcr = new DefaultListCellRenderer();
            dlcr.setHorizontalAlignment(DefaultListCellRenderer.CENTER);
            yearComboBox.setRenderer(dlcr);
            tableModel = (DefaultTableModel) customerDebtTable.getModel();
            tableModel.addColumn("Επώνυμο");
            tableModel.addColumn("Όνομα");
            tableModel.addColumn("Αιτιολογία");
            tableModel.addColumn("Χρέος");
            tableModel.addColumn("Πίστωση");
            tableModel.addColumn("Υπόλοιπο");
            tableModel.addColumn("Ημερομηνία");
            customerDebtTable.setRowHeight(h * 5 / 100);
            customerDebtTable.getTableHeader().setFont(new Font("Tahoma", Font.BOLD, 18));
            customerDebtTable.setShowGrid(true);
            customerDebtScrollPane.setLocation(w * 1 / 100, h * 13 / 100);
            customerDebtScrollPane.setSize(w * 88 / 100, h * 65 / 100);
            ArrayList<Integer> yearList = new ArrayList<Integer>();
            int i, j;
            int maxYear = execute.GetMaxYear(customerModel.id);
            debtModel = execute.SelectCustomersDebtByYear("select date, reason, xreosi, pistosi, ipolipo, debtYear from customersDebtByYear where id = " + customerModel.id);
            for (CustomerDeptModel item : debtModel) {
                if (yearList.size() == 0) {
                    deleteDebtYearButton.setVisible(true);
                    yearList.add(item.year);
                } else {
                    for (i = 0; i < yearList.size(); i++) {
                        if (yearList.get(i) == item.year) {
                            break;
                        } else {
                            if (yearList.get(yearList.size() - 1) == item.year) {
                                break;
                            } else {
                                yearList.add(item.year);
                            }
                        }
                    }
                }
                if (item.year == maxYear) {
                    row = new Vector();
                    row.add(customerModel.customerSurname);
                    row.add(customerModel.customerName);
                    row.add(item.reason);
                    row.add(numberFormatter.format(item.xreosi));
                    row.add(numberFormatter.format(item.pistosi));
                    row.add(numberFormatter.format(item.ipolipo));
                    row.add(item.date);
                    tableModel.addRow(row);
                }
            }
            for (i = 0; i < yearList.size(); ++i) {
                for (j = i + 1; j < yearList.size(); ++j) {
                    if (yearList.get(i) < yearList.get(j)) {
                        int temp = yearList.get(i);
                        yearList.set(i, yearList.get(j));
                        yearList.set(j, temp);
                    }
                }
            }
            for (i = 0; i < yearList.size(); ++i) {
                yearComboBox.addItem(String.valueOf(yearList.get(i)));
            }
            if (yearComboBox.getItemCount() > 0) {
                yearScrollPane.setVisible(true);
            }
            customerDebtScrollPane.setVisible(true);
            backArrowLabel.setVisible(true);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CustomerDebtAnalytical.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CustomerDebtAnalytical.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_formWindowOpened

